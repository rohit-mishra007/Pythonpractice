#12. Perform comparison operations (==, !=, >, <, >=, <=) between two numbers.

number1=int(input("Enter the number1 value = "))
number2=int(input("Enter the number2 values = "))

print("Number 1 == Number 2:", number1 == number2)
print(f"Number 1 == Number 2: {number1 == number2}")
print("Number 1 != Number 2:", number1 != number2)
print(f"Number 1!= Number 2: {number1 != number2}")
print("Number 1 > Number 2:", number1 > number2)
print(f"Number 1 > Number 2: {number1 > number2}")
print("Number 1 < Number 2:", number1 < number2)
print(f"Number 1 < Number 2: {number1 < number2}")
print("Number 1 >= Number 2:", number1 >= number2)
print(f"Number 1 >= Number 2: {number1 >= number2}")
print("Number 1 <= Number 2:", number1 <= number2)
print(f"Number 1 <= Number 2: {number1 <= number2}")
